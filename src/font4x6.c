const uint8_t *font4x6_glyph(unsigned char c)
{
    c = (unsigned char)toupper(c);
    for (size_t i = 0; i < KA_LEN(GLYPHS); i++)
        if (GLYPHS[i].ch == c)
            return GLYPHS[i].rows;
    return NULL;
}

int font4x6_width(const char *text)
{
    return (int)strlen(text) * 5;
}

void font4x6_draw(SDL_Surface *s, int x, int y, const char *text, KColor c)
{
    for (const char *p = text; *p; p++, x += 5) {
        const uint8_t *rows = font4x6_glyph((unsigned char)*p);
        if (!rows)
            continue;
        for (int ry = 0; ry < 6; ry++)
            for (int rx = 0; rx < 4; rx++)
                if (rows[ry] & (8 >> rx))
                    ksurf_pixel(s, x + rx, y + ry, c);
    }
}

void font4x6_draw_centered(SDL_Surface *s, KRect r, const char *text,
                           KColor c)
{
    int tw = font4x6_width(text) - 1; /* last char has no trailing space */
    int x = r.x + (r.w - tw) / 2;
    int y = r.y + (r.h - 6) / 2;
    font4x6_draw(s, x, y, text, c);
}
